// Objeto String. Incluye métodos como:
// length Devuelve la longitud de una cadena de texto.

var cadena = "hola";
var longitud = cadena.length;
console.log(longitud);



// toUpperCase() Devuelve la cadena en mayúscula.

var cadena = "hola";
var mayuscula = cadena.toUpperCase();
console.log(mayuscula);



// Objeto Array. Incluye métodos como:
// push() agrega elementos al final del array.

let animales = ["gato", "perro", "caballo"]
console.log(animales);

animales.push("vaca", "conejo")
console.log(animales);

// length Devuelve la longitud de un arreglo.

var animales = ["gato", "perro", "caballo"];
var longitud = animales.length;
console.log(longitud);

